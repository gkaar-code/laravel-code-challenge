use App\Models\Comment;
use App\Models\User;
use Illuminate\Auth\Access\HandlesAuthorization;
use Illuminate\Http\Response;
use Illuminate\Support\Facades\Auth;

class CommentPolicy
{
    use HandlesAuthorization;

    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function viewAny(?User $user)
    {
        return true;
    }

    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Comment  $comment
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function view(?User $user, Comment $comment)
    {
        // INFO: a comment can only be published if the post it belongs
        //       has already been published:
        //       You shouldn't even be able to add a comment to an unpublished post!
        if ($comment->is_published && $comment->post->is_published) {
            return true;
        }

        return $comment->author->is($user) ?: $this->deny(
            "Access denied to this post: {$comment->getRouteKey()}",
            Response::HTTP_FORBIDDEN
        );
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function create(User $user)
    {
        return $user->is(Auth::user()) ?: $this->deny(
            "Access denied to create a new comment.",
            Response::HTTP_FORBIDDEN
        );
    }

